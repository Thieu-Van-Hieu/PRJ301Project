public class EventType {
    private int typeId;
    private String typeName;

    public EventType() {
    }

    public EventType(int typeId, String typeName) {
        this.typeId = typeId;
        this.typeName = typeName;
    }

    public int getTypeId() {
        return typeId;
    }

    public String getTypeName() {
        return typeName;
    }     
}
